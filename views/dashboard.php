<!-- Tambahkan JavaScript untuk fungsi pagination -->
<script>
    document.addEventListener('DOMContentLoaded', function() {
        const table = document.querySelector('.table');
        const tbody = table.querySelector('tbody');
        const rows = Array.from(tbody.querySelectorAll('tr'));
        const pageSizeSelect = document.getElementById('page-size');
        const prevPageBtn = document.getElementById('prev-page');
        const nextPageBtn = document.getElementById('next-page');
        const startEntry = document.getElementById('start-entry');
        const endEntry = document.getElementById('end-entry');
        const totalEntries = document.getElementById('total-entries');

        let currentPage = 1;
        let pageSize = parseInt(pageSizeSelect.value);

        // Inisialisasi total entries
        totalEntries.textContent = rows.length;

        function showPage(page) {
            const start = (page - 1) * pageSize;
            const end = start + pageSize;

            // Sembunyikan semua rows
            rows.forEach(row => row.style.display = 'none');

            // Tampilkan rows untuk halaman ini
            rows.slice(start, end).forEach(row => row.style.display = '');

            // Update info entries
            startEntry.textContent = start + 1;
            endEntry.textContent = Math.min(end, rows.length);

            // Update pagination buttons
            updatePaginationButtons();

            // Update active page
            updateActivePage(page);
        }

        function updatePaginationButtons() {
            const totalPages = Math.ceil(rows.length / pageSize);
            const paginationUl = document.querySelector('.pagination');

            // Clear existing number buttons
            const numberButtons = paginationUl.querySelectorAll('.page-item:not(:first-child):not(:last-child)');
            numberButtons.forEach(button => button.remove());

            // Add new number buttons
            for (let i = 1; i <= totalPages; i++) {
                const li = document.createElement('li');
                li.className = `page-item ${i === currentPage ? 'active' : ''}`;
                li.innerHTML = `<a class="page-link" href="#" data-page="${i}">${i}</a>`;
                paginationUl.insertBefore(li, nextPageBtn.parentElement);
            }

            // Update prev/next buttons
            prevPageBtn.parentElement.classList.toggle('disabled', currentPage === 1);
            nextPageBtn.parentElement.classList.toggle('disabled', currentPage === totalPages);
        }

        function updateActivePage(page) {
            const buttons = document.querySelectorAll('.pagination .page-item');
            buttons.forEach(button => button.classList.remove('active'));
            const activeButton = document.querySelector(`.pagination .page-item a[data-page="${page}"]`);
            if (activeButton) {
                activeButton.parentElement.classList.add('active');
            }
        }

        // Event Listeners
        pageSizeSelect.addEventListener('change', function() {
            pageSize = parseInt(this.value);
            currentPage = 1;
            showPage(currentPage);
        });

        prevPageBtn.addEventListener('click', function(e) {
            e.preventDefault();
            if (currentPage > 1) {
                currentPage--;
                showPage(currentPage);
            }
        });

        nextPageBtn.addEventListener('click', function(e) {
            e.preventDefault();
            const totalPages = Math.ceil(rows.length / pageSize);
            if (currentPage < totalPages) {
                currentPage++;
                showPage(currentPage);
            }
        });

        document.querySelector('.pagination').addEventListener('click', function(e) {
            if (e.target.tagName === 'A' && e.target.dataset.page) {
                e.preventDefault();
                currentPage = parseInt(e.target.dataset.page);
                showPage(currentPage);
            }
        });

        // Inisialisasi tampilan
        showPage(1);
    });
</script>

